tuple1[0]
tuple1[0:3]


# set არის კონტეინერი რომელშიც შეგვიძლია მრავალი ელემენტი შევინახოთ და მისი დამახასიათებელი ბრჩხილების ტიპია {}. მას არ გააჩნია indexing, მაგრამ არის muttable (remove, add). შეგვიძლია ნებისმიერი მონაცემების ტიპის შენახვა. მასში რამოდენიმეჯერ ერთი და იგივე ელემენტის ჩაწერის მიუხედავად დუბლიკატებს არ გამოიტანს.

