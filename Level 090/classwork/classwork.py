# list არის კონტეინერი რომელშიც შეგვიძლია მრავალი ელემენტი შევინახოთ და მისი დამახასიათებელი ბრჩხილების ტიპია []. მას გააჩნია indexing, შესაბამისად slicing და არის muttable. შეგვიძლია ნებისმიერი მონაცემების ტიპის შენახვა.

