# tuple არის კონტეინერი რომელშიც შეგვიძლია მრავალი ელემენტი შევინახოთ და მისი დამახასიათებელი ბრჩხილების ტიპია (). მას გააჩნია indexing, შესაბამისად slicing, მაგრამ list-ისგან განსხვავებით არის immutable. შეგვიძლია ნებისმიერი მონაცემების ტიპის შენახვა.

